# Operadores logico 

#AND



print(True and True) # si ambos son verdaderes = true
print(True and False) #Si uno es verdadero = True
print(False and True) #Si uno es verdadero = True
print(False and True) # si ambos son falsos = False

#OR
print(True or True) # si ambos son verdaderes = true
print(True or False) #Si uno es verdadero = True
print(False or True) #Si uno es verdadero = True
print(False or True) # si ambos son falsos = False

#NOT
print(not True) # si es veradero = False
print(not False) # si es falso = verdadero


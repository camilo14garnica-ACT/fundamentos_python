# Operadores de asignacion 

x = 5 

x = x + 2 # suma y asignacion
x +=2
x -=3
print(x)

x = x * 4 # multiplicacion y asignacion
x *= 4
print(x)

x = x / 2  # division y division
x /= 2
print(x)

x = x % 3 # modulo y asignacion
x %= 3
print(x)

x = x ** 3 # Exponenciasion y asignacion
x **= 2
print(x)

x = 10
print(x)

# walrus operator (Python 3.8+) :=

print(x := 12) # asigna 10 y luego imprme 12.

